print("Program to find Rainbow colors")

cc = int(input("Enter a number beteen 1 - 7 : "))

if (cc == 1):
    print("Violet")
elif (cc == 2):
    print("Indigo")
elif (cc == 3):
    print("Blue")   
elif (cc == 4):
    print("Green")   
elif (cc == 5):
    print("Yellow")   
elif (cc == 6):
    print("Orange")   
elif (cc == 7):
    print("Red")   
else :
    print("Invalid Color Code")

